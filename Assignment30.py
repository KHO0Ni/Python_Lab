import numpy as np

# Create an array with values ranging from 2 to 10
array = np.arange(2, 11).reshape(3, 3)

# Print the 3x3 matrix
print("3x3 matrix with values ranging from 2 to 10:")
print(array)
'''output:
3x3 matrix with values ranging from 2 to 10:
[[ 2  3  4]
 [ 5  6  7]
 [ 8  9 10]]
'''