import numpy as np  # Import the numpy library and alias it as np

# Define the list
my_list = [1, 2, 3, 4, 5]

# Convert the list to a NumPy array
my_array = np.array(my_list)

# Display the NumPy array
print("The NumPy array is:", my_array)
